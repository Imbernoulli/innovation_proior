// TIER: trivial
// Perform nothing: empty tour. Cost = sum of all omission penalties = baseline B.
// Scores exactly 0.1 by construction.
#include <bits/stdc++.h>
using namespace std;
int main(){
    int P; long long Q,K;
    if(scanf("%d %lld %lld",&P,&Q,&K)!=3) return 0;
    long long x0,y0; scanf("%lld %lld",&x0,&y0);
    for(int i=0;i<P;i++){ long long a,b,cc,dd,m,c,w; scanf("%lld %lld %lld %lld %lld %lld %lld",&a,&b,&cc,&dd,&m,&c,&w); }
    printf("0\n");
    return 0;
}
